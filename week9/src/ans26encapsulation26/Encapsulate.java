package ans26encapsulation26;

public class Encapsulate {
    private String name;
    private int rollNo;
    private int age;

    public String getName() {
    return name; }
    public void setName(String name1){
        this.name = name1;  }
    public int getRollNo(){
        return rollNo; }
    public void setRollNo(int rollNo1){
        this.rollNo = rollNo1; }
    public int getAge(){
        return age;  }
    public void setAge(int age1){
        this.age = age1; }
}
